def greaterThan(a,b):
    if a>b:
        return True
    else:
        return False
def lessThan(c,d):
    if c<d:
        return True
    else:
        return False
def equalTo(e,f):
    if e==f:
        return True
    else:
        return False
def greaterOrEqual(g,h):
    if g>=h:
        return True
    else:
        return False
def lessOrEqual(i,j):
    if i<=j:
        return True
    else:
        return False

print(greaterThan(3,6))
print(lessThan(6,2))
print(equalTo(5,5))
print(greaterOrEqual(2,6))
print(lessOrEqual(8,3))
